    public static void Registration(String login, String lastname, String initials, int password)
    {
        
        if (ifUserExists(login))
        {
            outputLabel.setText("Пользователь с таким логином уже существует");
        }
        else
        {
            String addUser = "INSERT INTO Teachers (teacher_login, teacher_lastname, teacher_initials, teacher_pass) VALUES (?, ?, ?, ?)";
            try (Connection conn = DriverManager.getConnection("jdbc:sqlite:teacher.db");
                    PreparedStatement stmt = conn.prepareStatement(addUser))
            {
                String newPass = String.valueOf(password);
                stmt.setString(1, login);
                stmt.setString(2, lastname);
                stmt.setString(3, initials);
                stmt.setString(4, newPass);
                stmt.executeUpdate();
                SwingUtilities.invokeLater(() -> {
                    for (Window window : Window.getWindows()) {
                        if (window.isVisible() && window instanceof regForm) {
                            window.dispose();
                            break;
                            }
                        }
                    });
                openNext();
            }
            catch (Exception e)
            {
                e.printStackTrace();
            }
        }
             
    }
    private static void openNext()
    {
        authForm auth = new authForm();
        auth.setVisible(true);
    }
    
    public static boolean ifUserExists(String login)
    {
        String query = "SELECT 1 FROM Teachers WHERE teacher_login = ?";
        
        try (Connection conn = DriverManager.getConnection("jdbc:sqlite:teacher.db");
             PreparedStatement stmt = conn.prepareStatement(query))
        {
            stmt.setString(1, login);
            try (ResultSet rs = stmt.executeQuery())
            {
                return rs.next();
            }
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            return false;
        }
    }
