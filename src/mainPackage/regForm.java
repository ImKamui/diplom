    private void regButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regButtonActionPerformed
        login = loginField.getText();
        lastname = lastnameField.getText();
        initials = initialsField.getText();
        pass = new String(passField.getPassword());
        int hashPass = pass.hashCode();
        if (login.isEmpty() || lastname.isEmpty() || initials.isEmpty() || pass.isEmpty())
        {
            outputLabel.setText("Поля не могут быть пустыми");
        }
        else
        {
            Registration(login, lastname, initials, hashPass);
        }
    }//GEN-LAST:event_regButtonActionPerformed

